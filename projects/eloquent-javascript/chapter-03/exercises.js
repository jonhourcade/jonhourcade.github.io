////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(val1, val2) {
  return Math.min(val1, val2);
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(value) {
  return (value % 2 === 0);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) {
  
  let count = 0;
  
  for (let i = 0; i < string.length; ++i) {
    if (string[i] === character) ++count;
  }
  
  return count;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  
  let count = 0;
  
  for (let i = 0; i < string.length; ++i) {
    if (string[i] === 'B') ++count;
  }
  
  return count;

}

