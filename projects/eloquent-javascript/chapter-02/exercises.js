function triangles(number) {
  
  let triangle = [];
  
  for (let i = 1; i <= number; ++i) {
    
    let string = "";
    
    for (let j = 0; j < i; ++j) {
      
      string += '#';
      
    }
    
    triangle.push(string);
    
  }
  
  for (let row of triangle) {
    console.log(row);
  }

}
